# WAP in python to display the name of the day according to the number given by the user

no = int(input("Enter Number to Print Day: "))

if no == 1:
    print("Sunday")
elif no == 2:
    print("Monday")
elif no == 3:
    print("Tuesday")
elif no == 4:
    print("Wednesday")
elif no == 5:
    print("Thursday")
elif no == 6:
    print("Friday")
elif no == 7:
    print("Saturday")
